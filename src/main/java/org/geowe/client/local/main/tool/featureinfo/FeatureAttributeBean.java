package org.geowe.client.local.main.tool.featureinfo;

import java.io.Serializable;

/**
 * 
 * @author geowe
 *
 */
public class FeatureAttributeBean implements Serializable {
	private static final long serialVersionUID = 77109489576386882L;
	
	private String attributeName;
	private String attributeValue;

	public FeatureAttributeBean(String name, String value) {
		this.attributeName = name;
		this.attributeValue = value;
	}

	public String getAttributeName() {
		return attributeName;
	}
	public void setAttributeName(String attributeName) {
		this.attributeName = attributeName;
	}
	public String getAttributeValue() {
		return attributeValue;
	}
	public void setAttributeValue(String attributeValue) {
		this.attributeValue = attributeValue;
	}
	

	@Override
	public String toString() {
		return "Layer [" + attributeName + "]";
	}
	
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((attributeName == null) ? 0 : attributeName.hashCode());
		return result;
	}
	
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		FeatureAttributeBean other = (FeatureAttributeBean) obj;
		if (attributeName == null) {
			if (other.attributeName != null)
				return false;
		} else if (!attributeName.equals(other.attributeName))
			return false;
		return true;
	}	 
}
